async function seedUsers() {
  console.log("🚀 Starting to seed 20 fraud test users...\n");

  for (let i = 0; i < fraudUsers.length; i++) {
    const user = fraudUsers[i];
    try {
      await addDoc(collection(db, "users"), {
        uid: `fraud_test_${i + 1}`,
        name: user.name,
        upiId: user.upiId,
        email: `${user.upiId.split("@")[0]}@test.com`,
        photoURL: null,
        balance: user.balance,
        modelData: user.modelData,
        transactionDetails: { Label: 1 },
        isTestUser: true,
        scenario: user.scenario,
      });
      console.log(`✅ [${i + 1}/20] Added: ${user.name} — ${user.upiId} (${user.scenario})`);
    } catch (err) {
      console.error(`❌ [${i + 1}/20] Failed: ${user.name} —`, err.message);
    }
  }

  console.log("\n🎉 Done! All 20 fraud test users added to Firestore.");
  console.log("\n📋 Test UPI IDs to use in Send Money:");
  fraudUsers.forEach((u, i) => {
    console.log(`  ${i + 1}. ${u.upiId.padEnd(25)} → ${u.scenario}`);
  });
  process.exit(0);
}

seedUsers();
